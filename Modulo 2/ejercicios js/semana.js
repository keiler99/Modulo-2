let dia = 5;
let nombredia;

switch(dia){
    case 1:
        nombredia = "lunes";
        break;
    case 2:
        nombredia = "martes";
        break;
    case 3:
        nombredia = "miercoles";
        break;
    case 4:
        nombredia = "jueves";
        break;
    case 5:
        nombredia = "viernes";
        break;
    case 6:
        nombredia = "sabado";
        break;
    case 7:
        nombredia = "domingo";
        break;
    default:
        nombredia = "dia invalido";
}
console.log(nombredia)